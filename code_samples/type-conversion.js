// With great power comes great responsibility...
// just because you can convert types on the fly, doesn't mean you should
// proceed with caution

// Type conversion

// starts as a number
var a = 26;
console.log(typeof a); // starts at an integer

// change to string
a = "Carmen";
console.log(typeof a); // becomes a string

// change to object
a = {
	name: "Carmen",
  age: 26
}
console.log(typeof a); // becomes and object